package ViewModels;

import java.math.BigDecimal;

/**
 *
 * @author dungp
 */
public class ViewBanHangModels {

    private String maSP;
    private String tenSP;
    private int soLuong;
    private BigDecimal donGia;
    private BigDecimal thanhTien;

    public ViewBanHangModels() {
    }

    public ViewBanHangModels(String maSP, String tenSP, int soLuong, BigDecimal donGia, BigDecimal thanhTien) {
        this.maSP = maSP;
        this.tenSP = tenSP;
        this.soLuong = soLuong;
        this.donGia = donGia;
        this.thanhTien = thanhTien;
    }

    public String getMaSP() {
        return maSP;
    }

    public void setMaSP(String maSP) {
        this.maSP = maSP;
    }

    public String getTenSP() {
        return tenSP;
    }

    public void setTenSP(String tenSP) {
        this.tenSP = tenSP;
    }

    public int getSoLuong() {
        return soLuong;
    }

    public void setSoLuong(int soLuong) {
        this.soLuong = soLuong;
    }

    public BigDecimal getDonGia() {
        return donGia;
    }

    public void setDonGia(BigDecimal donGia) {
        this.donGia = donGia;
    }

    public BigDecimal getThanhTien() {
        return thanhTien;
    }

    public void setThanhTien(BigDecimal thanhTien) {
        this.thanhTien = thanhTien;
    }

    public BigDecimal thanhTien() {
        BigDecimal soLuong1 = new BigDecimal(soLuong);
        BigDecimal thanhTien = soLuong1.multiply(donGia);
        return thanhTien;
    }

}
